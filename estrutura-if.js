// Estruturas de controle de fluxo

let x = 10;

// if
if (x === 10) {
  console.log("é igual a 10");
}

x = 5;

// if - else
if (x === 10) {
  console.log("é igual a 10");
} else {
  console.log("é diferente de 10");
}

// if - else if - else
if (x < 10) {
  console.log("é menor que 10");
} else if (x > 10) {
  console.log("é maior que 10");
} else {
  console.log("é igual a 10");
}
